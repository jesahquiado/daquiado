    /**
     * Make sure extra profile columns exist on the users table.
     */
    private void ensureProfileColumns() {
        try (Connection conn = config.connectDB();
             Statement stmt = conn != null ? conn.createStatement() : null) {

            if (stmt == null) {
                return;
            }

            // Try to add columns; ignore errors if they already exist
            try {
                stmt.executeUpdate("ALTER TABLE users ADD COLUMN gender TEXT");
            } catch (SQLException ignored) {}
            try {
                stmt.executeUpdate("ALTER TABLE users ADD COLUMN address TEXT");
            } catch (SQLException ignored) {}
            try {
                stmt.executeUpdate("ALTER TABLE users ADD COLUMN phone TEXT");
            } catch (SQLException ignored) {}
        } catch (SQLException e) {
            System.out.println("Error ensuring profile columns: " + e.getMessage());
        }
    }

    /**
     * Load the current user's profile details into the form.
     */
    private void loadProfileFromDatabase() {
        if (CurrentUser.id <= 0) {
            return;
        }

        String sql = "SELECT full_name, gender, address, phone, email, password "
                   + "FROM users WHERE id = ?";

        try (Connection conn = config.connectDB();
             PreparedStatement pstmt = conn != null ? conn.prepareStatement(sql) : null) {

            if (pstmt == null) {
                return;
            }

            pstmt.setInt(1, CurrentUser.id);

            try (ResultSet rs = pstmt.executeQuery()) {
                if (rs.next()) {
                    jTextField1.setText(rs.getString("full_name"));
                    jTextField2.setText(rs.getString("gender"));
                    jTextField3.setText(rs.getString("address"));
                    jTextField4.setText(rs.getString("phone"));
                    jTextField5.setText(rs.getString("email"));
                    jTextField6.setText(rs.getString("password"));
                }
            }
        } catch (SQLException e) {
            System.out.println("Error loading profile: " + e.getMessage());
        }
    }

    /**
     * Save profile changes back to the users table.
     */
    private void saveProfile() {
        if (CurrentUser.id <= 0) {
            JOptionPane.showMessageDialog(this,
                    "No logged-in user profile to save.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        String fullName = jTextField1.getText().trim();
        String gender = jTextField2.getText().trim();
        String address = jTextField3.getText().trim();
        String phone = jTextField4.getText().trim();
        String email = jTextField5.getText().trim();
        String password = jTextField6.getText();

        if (fullName.isEmpty() || email.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Full name, email, and password are required.",
                    "Validation Error",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        String sql = "UPDATE users "
                   + "SET full_name = ?, gender = ?, address = ?, phone = ?, email = ?, password = ? "
                   + "WHERE id = ?";

        try (Connection conn = config.connectDB();
             PreparedStatement pstmt = conn != null ? conn.prepareStatement(sql) : null) {

            if (pstmt == null) {
                JOptionPane.showMessageDialog(this,
                        "Unable to connect to the database.",
                        "Connection Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            pstmt.setString(1, fullName);
            pstmt.setString(2, gender);
            pstmt.setString(3, address);
            pstmt.setString(4, phone);
            pstmt.setString(5, email);
            pstmt.setString(6, password);
            pstmt.setInt(7, CurrentUser.id);

            pstmt.executeUpdate();

            // Keep CurrentUser in sync
            CurrentUser.fullName = fullName;
            CurrentUser.email = email;

            JOptionPane.showMessageDialog(this,
                    "Profile information saved successfully.",
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Error saving profile: " + e.getMessage(),
                    "Database Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

